using System;
using System.Collections.Generic;
using System.Text;

using System.Reflection;

namespace Sigbit.Common
{
    /// <summary>
    /// 枚举的扩展操作方法
    /// </summary>
    public class EnumExUtil
    {
        /// <summary>
        /// 
        /// </summary>
        /// <param name="eEnum"></param>
        /// <returns></returns>
        public static string ToDescString(Enum eEnum)
        {
            return eEnum.ToDescString();
        }

        /// <summary>
        /// 
        /// </summary>
        /// <param name="eEnum"></param>
        /// <returns></returns>
        public static string ToCodeString(Enum eEnum)
        {
            return eEnum.ToCodeString();
        }

        /// <summary>
        /// 
        /// </summary>
        /// <param name="eEnum"></param>
        /// <param name="sCode"></param>
        /// <returns></returns>
        public static string CodeToDesc(Enum eEnum, string sCode)
        {
            return eEnum.CodeToDesc(sCode);
        }

        /// <summary>
        /// 
        /// </summary>
        /// <param name="eEnum"></param>
        /// <param name="sCode"></param>
        /// <returns></returns>
        public static object CodeToEnum(Enum eEnum, string sCode)
        {
            return eEnum.CodeToEnum(sCode);
        }

        /// <summary>
        /// 
        /// </summary>
        /// <param name="eEnum"></param>
        /// <returns></returns>
        public static CodeTableBase ToCodeTable(Enum eEnum)
        {
            return eEnum.ToCodeTable();
        }


    }
}
