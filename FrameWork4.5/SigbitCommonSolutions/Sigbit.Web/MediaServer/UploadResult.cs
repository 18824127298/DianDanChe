using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Sigbit.Web.MediaServer
{
    /// <summary>
    /// 文件上传结果
    /// </summary>
    public class FileUploadResult
    {
        private string _originalName = "";
        /// <summary>
        /// 原始文件名称
        /// </summary>
        public string OriginalName
        {
            get { return _originalName; }
            set { _originalName = value; }
        }


       

        private string _fileType = "";
        /// <summary>
        /// 文件类型
        /// </summary>
        public string FileType
        {
            get { return _fileType; }
            set { _fileType = value; }
        }


        private int _fileSize = 0;
        /// <summary>
        /// 文件大小
        /// </summary>
        public int FileSize
        {
            get { return _fileSize; }
            set { _fileSize = value; }
        }


        private bool _isSucc = false;
        /// <summary>
        /// 是否成功
        /// </summary>
        public bool IsSucc
        {
            get { return _isSucc; }
            set { _isSucc = value; }
        }


        private string _fileName = "";
        /// <summary>
        /// 文件名称
        /// </summary>
        public string FileName
        {
            get { return _fileName; }
            set { _fileName = value; }
        }

        private MediaServerPath _resultPath = new MediaServerPath();
        /// <summary>
        /// 结果路径
        /// </summary>
        public MediaServerPath ResultPath
        {
            get { return _resultPath; }
            set { _resultPath = value; }
        }


        private MediaServerPath _thumbnailPath = new MediaServerPath();
        /// <summary>
        /// 缩略图路径
        /// </summary>
        public MediaServerPath ThumbnailPath
        {
            get { return _thumbnailPath; }
            set { _thumbnailPath = value; }
        }     

        private string _resultMessage = "";
        /// <summary>
        /// 结果信息
        /// </summary>
        public string ResultMessage
        {
            get { return _resultMessage; }
            set { _resultMessage = value; }
        }

    }
}
