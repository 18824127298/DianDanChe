using System;
using System.Collections.Generic;
using System.Text;

namespace Sigbit.Framework.Task
{
    /// <summary>
    ///  封装任务的实现，每个实例是一个任务
    /// </summary>
    public class PrivateTask
    {
        private string _taskSubject = "";
        /// <summary>
        /// 任务的主题
        /// </summary>
        public string TaskSubject
        {
            get { return _taskSubject; }
            set { _taskSubject = value; }
        }

        private string _taskPrompt = "";
        /// <summary>
        /// 任务的文本提示
        /// </summary>
        public string TaskPrompt
        {
            get { return _taskPrompt; }
            set { _taskPrompt = value; }
        }

        private string _linkUrl = "";
        /// <summary>
        /// 相应的链接
        /// </summary>
        public string LinkUrl
        {
            get { return _linkUrl; }
            set { _linkUrl = value; }
        }

        private string _taskTime = "";
        /// <summary>
        /// 即将发生的时间
        /// </summary>
        public string TaskTime
        {
            get { return _taskTime; }
            set { _taskTime = value; }
        }
    }
}
